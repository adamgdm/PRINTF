#include "Library.h"

void	ft_putu_fd(unsigned int n, int fd)
{		
	if (n > 9)
		{
			ft_putu_fd(n / 10, fd);
			ft_putu_fd(n % 10, fd);
		}
		else if (n < 10)
		{
			ft_putchar_fd(n + '0', fd);
		}
}

